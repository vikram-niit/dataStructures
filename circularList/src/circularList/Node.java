package circularList;

public class Node
{
	private Node node;
	private int data;

	public int getData()
	{
		return this.data;
	}

	public void setData(int data)
	{
		this.data = data;
	}

	public Node getNode()
	{
		return this.node;
	}

	public void setNode(Node node)
	{
		this.node = node;
	}
}
