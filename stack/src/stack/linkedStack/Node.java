package stack.linkedStack;

public class Node
{
	private Node node;
	private String data;

	public String getData()
	{
		return this.data;
	}

	public void setData(String data)
	{
		this.data = data;
	}

	public Node getNode()
	{
		return this.node;
	}

	public void setNode(Node node)
	{
		this.node = node;
	}
}
